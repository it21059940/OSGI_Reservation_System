package hotelreservationserviceconsumer;

public class CustomerFeedback {
	
	private String name;
	private int userFriendlyRating;
	private int usefulnessRating;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getUserFriendlyRating() {
		return userFriendlyRating;
	}
	
	public void setUserFriendlyRating(int userFriendlyRating) {
		this.userFriendlyRating = userFriendlyRating;
	}
	
	public int getUsefulnessRating() {
		return usefulnessRating;
	}
	
	public void setUsefulnessRating(int usefulnessRating) {
		this.usefulnessRating = usefulnessRating;
	}
	
	

}
